package com.fro.wifi_temhumcase;

public class Data {

	//温度
	private int tem;
	//湿度
	private int hum;
	//光照
	private int sun;
	public int getTem() {
		return tem;
	}
	public void setTem(int tem) {
		this.tem = tem;
	}
	public int getHum() {
		return hum;
	}

	public int getSun() {
		return sun;
	}

	public void setSun(int sun) {
		this.sun = sun;
	}

	public void setHum(int hum) {
		this.hum = hum;
	}
	
	
}
